export function getResponseCategory(message: string): string {
  const msg = message.toLowerCase();
  
  if (msg.includes('cześć') || msg.includes('hej') || msg.includes('witaj') || msg.includes('hello') || msg.includes('dzień dobry')) {
    return 'greetings';
  }
  
  if (msg.includes('usług') || msg.includes('offer') || msg.includes('co robi') || msg.includes('specjalizuj')) {
    return 'services';
  }
  
  if (msg.includes('cen') || msg.includes('koszt') || msg.includes('ile') || msg.includes('prici') || msg.includes('budżet')) {
    return 'pricing';
  }
  
  if (msg.includes('chatbot') || msg.includes('ai') || msg.includes('sztuczn') || msg.includes('bot') || msg.includes('inteligenc')) {
    return 'ai_chat';
  }
  
  if (msg.includes('kontakt') || msg.includes('spotkanie') || msg.includes('rozmow') || msg.includes('telefon') || msg.includes('mail')) {
    return 'contact';
  }
  
  return 'general';
}

export function getFallbackResponse(message: string): string {
  const category = getResponseCategory(message);
  const responses = fallbackResponses[category as keyof typeof fallbackResponses];
  
  // Wybierz losową odpowiedź z kategorii
  const randomIndex = Math.floor(Math.random() * responses.length);
  return responses[randomIndex];
}